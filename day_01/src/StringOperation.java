
public class StringOperation {
	public static void main(String[] args) {
		// 문자열 결합 연산
		// Java의 문자열은 + 연산을 허용
		// 어떠한 타입의 값이 문자열과 + 연산이 되면
		// 문자열 결합이 된다.
		// 10 + " + " + 20
		System.out.println(10 + " + " + 20);
		System.out.println(10 + " + " + 20 + " = " + 10 + 20);
		System.out.println(10 + " + " + 20 + " = " + (10 + 20));
	}
}
